class Solution {

    public double findMedianSortedArrays(int[] nums1, int[] nums2) {
        int i1 = nums1.length / 2;
        int n1 = nums1[i1];
        int i2 = nums2.length - i1;
        int n2 = nums2[i2];
        if(n1 == n2){
            return n1;
        }
        boolean lg = n1 > n2;
        while (lg == n1 > n2){
            if(lg){
                n1 = nums1[i1--];
                n2 = nums2[i2++];
            } else {
                n1 = nums1[i1++];
                n2 = nums2[i2--];
            }
        }
        return 0;
    }
}
